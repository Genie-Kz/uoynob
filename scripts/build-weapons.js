// No 範囲 → 武器種
function weaponType(no) {
  if (no <= 21) return '剣';
  if (no <= 36) return 'ヤリ';
  if (no <= 49) return 'オノ';
  if (no <= 64) return 'ハンマー';
  if (no <= 78) return 'ムチ';
  if (no <= 95) return 'ツメ';
  return '杖';
}

const weapons = TABLE.trim()
  .split('\n')
  .map((line) => {
    const [no, name, atk, def, agi, wis] = line.split(/\s+/);
    const n = Number(no);
    return {
      no: n,
      name,
      type: weaponType(n),
      攻撃力: Number(atk),
      守備力: Number(def),
      素早さ: Number(agi),
      賢さ: Number(wis),
    };
  });

fs.writeFileSync(
  path.join(scriptDir, '..', 'public', 'data', 'weapons.json'),
  JSON.stringify(weapons),
  'utf8',
);
console.error(`weapons: ${weapons.length}`);
